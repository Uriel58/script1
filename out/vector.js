function map2(a,b)                                 // (1) 
{                                                  // (1) 
  let alen=len(a);                                 // (2) 
  let c=array(alen);                               // (3) 
  for (let i of range(0,alen))                     // (4) 
  {                                                // (4) 
    c[i]=(a[i]+b[i]);                              // (5) 
  };                                               // (4) 
                                                   // (4) 
  return c;                                        // (7) 
}                                                  // (1) 
function dot(a,b)                                  // (10) 
{                                                  // (10) 
  let alen=len(a);                                 // (11) 
  let r=0;                                         // (12) 
  for (let i of range(0,alen)) r=(r+(a[i]*b[i]));  // (14) 
                                                   // (13) 
  return r;                                        // (15) 
}                                                  // (10) 
function neg(a)                                    // (18) 
{                                                  // (18) 
  return map(a,(a)=>- a);                          // (19) 
}                                                  // (18) 
function add(a,b)                                  // (22) 
{                                                  // (22) 
  return map2(a,b,(a,b)=>(a+b));                   // (23) 
}                                                  // (22) 

