export function map2(a,b,f)                        // (1) 
{                                                  // (1) 
    let alen=len(a);                               // (2) 
    let c=array(alen);                             // (3) 
    for (let i of range(0,alen))                   // (4) 
    {                                              // (4) 
        c[i]=f(a[i],b[i]);                         // (5) 
    };                                             // (4) 
                                                   // (4) 
    return c;                                      // (7) 
}                                                  // (1) 
export function dot(a,b)                           // (10) 
{                                                  // (10) 
    let alen=len(a);                               // (11) 
    let r=0;                                       // (12) 
    for (let i of range(0,alen))                   // (13) 
    {                                              // (13) 
        r=(r+(a[i]*b[i]));                         // (14) 
    };                                             // (13) 
                                                   // (13) 
    return r;                                      // (16) 
}                                                  // (10) 
export function neg(a)                             // (19) 
{                                                  // (19) 
    return map(a,(a)=>- a);                        // (20) 
}                                                  // (19) 
export function add(a,b)                           // (23) 
{                                                  // (23) 
    return map2(a,b,(a,b)=>(a+b));                 // (24) 
}                                                  // (23) 

